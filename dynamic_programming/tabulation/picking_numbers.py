def pickingNumbers(a):
    # Write your code here
    lengths = [1] * len(a)

    for i in range(1, len(a)):
        subprobs = [lengths[k] for k in range(i) if abs(a[i] - a[k]) in [1, 0]]
        lengths[i] = 1 + max(subprobs, default=0)

    return lengths


def pickingNumbers(a):
    # Write your code here
    lengths = []

    for i in range(1, len(a)):
        less_count = 1
        greater_count = 1
        for k in range(i):
            if a[k] == a[i]:
                less_count += 1
                greater_count += 1
            elif a[k] == a[i] - 1:
                less_count += 1
            elif a[k] == a[i] + 1:
                greater_count += 1
            else:
                continue

        lengths.append(max([greater_count, less_count]))

    return max(lengths)


